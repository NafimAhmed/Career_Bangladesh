package com.careers.app;





public class PostApplicat {
    private String id;
    private String jobID;
    private String email;
    private String name;
    private String jobTitle;
    private String organization;
    private String category;
    private String applicationDate;
    private String postersEmail;
    private String expectedSalary;

    public String getID() { return id; }
    public void setID(String value) { this.id = value; }

    public String getJobID() { return jobID; }
    public void setJobID(String value) { this.jobID = value; }

    public String getEmail() { return email; }
    public void setEmail(String value) { this.email = value; }

    public String getName() { return name; }
    public void setName(String value) { this.name = value; }

    public String getJobTitle() { return jobTitle; }
    public void setJobTitle(String value) { this.jobTitle = value; }

    public String getOrganization() { return organization; }
    public void setOrganization(String value) { this.organization = value; }

    public String getCategory() { return category; }
    public void setCategory(String value) { this.category = value; }

    public String getApplicationDate() { return applicationDate; }
    public void setApplicationDate(String value) { this.applicationDate = value; }

    public String getPostersEmail() { return postersEmail; }
    public void setPostersEmail(String value) { this.postersEmail = value; }

    public String getExpectedSalary() { return expectedSalary; }
    public void setExpectedSalary(String value) { this.expectedSalary = value; }
}
